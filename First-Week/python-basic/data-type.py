# Data Types Example

# Integer
a = 10
print("Integer:", a)

# Float
b = 3.14
print("Float:", b)

# String
name = "Suresh"
print("String:", name)

# Boolean
is_active = True
print("Boolean:", is_active)

# List
numbers = [1, 2, 3, 4]
print("List:", numbers)

# Tuple
t = (10, 20, 30)
print("Tuple:", t)

# Dictionary
student = {"name": "Suresh", "age": 20}
print("Dictionary:", student)

# Type checking
print("Type of a:", type(a))